// AND Operator (&&) Example
let age = 20;
let hasPermission = true;

if (age >= 18 && hasPermission) {
  console.log("You are an adult and have permission.");
} else {
  console.log("Either you're not an adult or you don't have permission.");
}

// Output: "You are an adult and have permission."

// OR Operator (||) Example
let age2 = 16;
let hasPermission2 = true;

if (age2 >= 18 || hasPermission2) {
  console.log("You can proceed.");
} else {
  console.log("You can't proceed.");
}

// Output: "You can proceed."
